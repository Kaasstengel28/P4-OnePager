dit kunnen wij bereiken door het circuit playground zijn infra rood scanner en lezer. Met dit kunnen wij een circuit playground in de
        tegel stoppen en een in de stok zelf. Helaas was het niet mogelijk om een zelf gemaakte audio file te gebruiken in de playground
        dus zijn wij, maar geland om een heel "duidelijk" geluidje. Verder heeft de tegel nog wel ribbels zodat hij gewoon kan functioneren
